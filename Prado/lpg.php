con los desastrosos resultados de la derrota de nuestro Ejército Peruano-Boliviano, Leoncio
Prado sigue luchando y resistiendo al enemigo; hasta caer prisionero y herido en Tarata el
21 de Julio de 1880, siendo llevado a Chile a la prisión de “San Bernardo” en donde fue
puesto en libertad en Febrero de 1882, bajo la palabra de no tomar las armas contra Chile.
Al llegar a Lima, contempla con profunda amargura la triste y aflictiva realidad de la Capital
comprobando la crueldad de los chilenos. Viaja a su tierra natal
Huánuco y forma nuevamente un cuerpo guerrillero e inicia estratégicos ataques al enemigo,
recorriendo diversos pueblos y aumentando sus contingentes hasta llegar a la batalla de
Huamachuco; el 10 de Julio de 1883 cae herido en la pierna izquierda por la explosión de
una granada.

                     
