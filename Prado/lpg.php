El Presidente del Perú Don Manuel Pardo,
enterado de la excelente trayectoria del joven
Leoncio Prado lo selecciona con otros
destacados estudiantes y los envía a los
Estados Unidos de Norte América a la ciudad
de Richmond para estudiar Ingeniería; en esta ciudad conoce a patriotas cubanos que
luchan por la independencia de su Patria y se une a ellos, viajando a Cuba para formar parte
en la lucha libertaria en 1874, acompañado por sus hermanos Justo y Glosio. En esta
cruenta lucha, Leoncio Prado concibe la idea de capturar un buque español diciendo “Los
buques con que debemos combatir están con bandera española”. El 7 de Noviembre de
1876 llega a Puerto Plata (Cuba) el Vapor español, “Moctezuma”; Leoncio Prado, había
seleccionado doce hombres y con ellos se embarca; a las seis de la tarde salen de sus
camarotes y toman por sorpresa a la tripulación trabándose en difícil lucha y dando muerte al
Capitán Cacho. Leoncio Prado, después de haber tomado la nave le cambia el nombre por
el de “Céspedes” en honor a un patriota cubano; por este hecho se considera a Leoncio
Prado como “fundador de la Marina de Guerra de Cuba”.
Pero en la patria lejana del Perú las cosas no andaban bien, Chile que se había preparado
cautelosamente armando un poderoso ejército, declarando la guerra al Perú el 5 de Abril de
1879.
En el Perú, Leoncio Prado recibe la orden de armar y comandar un cuerpo de guerrilleros,
participando en diversos combates en el sur hasta llegar a la batalla del “Alto del Alianza” 
